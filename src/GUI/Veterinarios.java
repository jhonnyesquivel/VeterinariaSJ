    private void cmdEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdEliminarActionPerformed

        Veterinario buscarVeterinario = postgres.buscarVeterinario(jTextField20.getText());

        if (buscarVeterinario != null) {
            txtNombre.setText(buscarVeterinario.getNombre());
            txtEspecialidad.setText(buscarVeterinario.getEspecialidad());
            int op = JOptionPane.showConfirmDialog(null, "¬øEsta seguro de eliminar este Veterinario?", "Advertencia", JOptionPane.WARNING_MESSAGE);
            if (op == JOptionPane.YES_OPTION) {
                postgres.eliminarVeterinario(jTextField20.getText());
                this.limpiar();
            }
        } else {
            JOptionPane.showMessageDialog(null, "El ID especificado no existe en la base de datos", "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_cmdEliminarActionPerformed


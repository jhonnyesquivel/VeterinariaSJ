    private void cmdEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdEliminarActionPerformed
        Mascota mascota = postgres.buscarMascota(jTextField1.getText());
        try {
            Integer.parseInt("" + mascota.getCliente_documento());
            txtNombre.setText(mascota.getNombre());
            txtRaza.setText(mascota.getRaza());
            txtdocumento.setText("" + mascota.getCliente_documento());
            txtpeso.setText("" + mascota.getPeso());
            combosexo.setSelectedItem(mascota.getSexo());
            SimpleDateFormat formatoDelTexto = new SimpleDateFormat("yyyy-MM-dd");
            String strFecha = mascota.getFecha_nacimiento();
            Date fecha = null;
            try {
                fecha = formatoDelTexto.parse(strFecha);
            } catch (ParseException ex) {
            }
            txtFecha.setText(fecha.toLocaleString().split(" ")[0]);


            int op = JOptionPane.showConfirmDialog(null, "Esta seguro de eliminar esta mascota?", "Advertencia", JOptionPane.WARNING_MESSAGE);
            if (op == JOptionPane.YES_OPTION) {
                if (postgres.eliminarMascota(jTextField1.getText())) {
                    JOptionPane.showMessageDialog(null, "Mascota Eliminada", "Ok", JOptionPane.INFORMATION_MESSAGE);
                    this.limpiar();
                }
            }
        } catch (NullPointerException e) {
            JOptionPane.showMessageDialog(null, "El 'ID' no existe en su base de datos\n" + e.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
        }




    }//GEN-LAST:event_cmdEliminarActionPerformed


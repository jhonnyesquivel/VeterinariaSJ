    private void cmdBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdBuscarActionPerformed

        Mascota mascota = postgres.buscarMascota(jTextField1.getText());
        try {
            Integer.parseInt("" + mascota.getCliente_documento());
            txtNombre.setText(mascota.getNombre().trim());
            txtRaza.setText(mascota.getRaza().trim());
            txtdocumento.setText("" + mascota.getCliente_documento());
            txtpeso.setText("" + mascota.getPeso());
            combosexo.setSelectedItem(mascota.getSexo().trim());
            SimpleDateFormat formatoDelTexto = new SimpleDateFormat("yyyy-MM-dd");
            String strFecha = mascota.getFecha_nacimiento();
            Date fecha = null;
            try {
                fecha = formatoDelTexto.parse(strFecha);
            } catch (ParseException ex) {
            }
            txtFecha.setText(fecha.toLocaleString().split(" ")[0]);
            JOptionPane.showMessageDialog(null, "Busqueda Exitosa.", "Bien", JOptionPane.INFORMATION_MESSAGE);

        } catch (NullPointerException e) {
            JOptionPane.showMessageDialog(null, "El id buscado no existe en su base de datos", "ERROR", JOptionPane.ERROR_MESSAGE);

        }




    }//GEN-LAST:event_cmdBuscarActionPerformed

    private void cmdAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdAgregarActionPerformed


        Date a = new Date(txtFecha.getText());

        if (a.after(new Date())) {
            JOptionPane.showMessageDialog(null, txtNombre.getText()+ " aun no ha nacido", "ERROR", JOptionPane.ERROR_MESSAGE);


            return;
        }


        if (!jTextField1.getText().trim().equals("")) {
            try {
                String fecha[] = txtFecha.getText().split("/");
                String dat = fecha[2] + "-" + fecha[1] + "-" + fecha[0];
                java.sql.Date date = java.sql.Date.valueOf(dat);

                postgres.agregarMascota(jTextField1.getText().trim(),
                        new Long(txtdocumento.getText()),
                        date,
                        txtNombre.getText().trim(),
                        combosexo.getSelectedItem().toString(),
                        txtRaza.getText().trim(),
                        Float.parseFloat(txtpeso.getText().replace(",", ".")));
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Hay un caracter de texto en un campo numerico\n" + e.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        }



    }//GEN-LAST:event_cmdAgregarActionPerformed


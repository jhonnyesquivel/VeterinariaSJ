    private void cmdAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdAgregarActionPerformed

        try {
            String fecha[] = jFormattedTextField1.getText().split("/");
            String dat = fecha[2] + "-" + fecha[1] + "-" + fecha[0];
            java.sql.Date date = java.sql.Date.valueOf(dat);
            if (date.before(new Date())) {
                new PostgreSQL().agregarGasto(jTextField4.getText(), date,
                        Double.parseDouble(jtextmonto.getText()), jtextDescrip.getText());
            } else {
                JOptionPane.showMessageDialog(null, "Estamos a "+ new Date(), "ERROR",
                        JOptionPane.ERROR_MESSAGE);

            }


        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "ERROR",
                    JOptionPane.ERROR_MESSAGE);
        }



    }//GEN-LAST:event_cmdAgregarActionPerformed


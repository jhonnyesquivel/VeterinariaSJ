    private void cmdBuscar3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdBuscar3ActionPerformed


        try {
            String[] gasto = new PostgreSQL().buscarGasto(jTextField4.getText());
            jtextmonto.setText(gasto[1]);
            jtextDescrip.setText(gasto[2]);

            SimpleDateFormat formatoDelTexto = new SimpleDateFormat("yyyy-MM-dd");
            String strFecha = gasto[3];
            Date fecha = null;
            try {
                fecha = formatoDelTexto.parse(strFecha);
            } catch (ParseException ex) {
            }
            jFormattedTextField1.setText(fecha.toLocaleString().split(" ")[0]);
            JOptionPane.showMessageDialog(null, "Busqueda Exitosa.", "Bien", JOptionPane.INFORMATION_MESSAGE);

        } catch (NullPointerException e) {
            JOptionPane.showMessageDialog(null, "El id buscado no existe en su base de datos", "ERROR", JOptionPane.ERROR_MESSAGE);
        }


   }//GEN-LAST:event_cmdBuscar3ActionPerformed


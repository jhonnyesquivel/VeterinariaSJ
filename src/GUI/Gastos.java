    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        SQL postgres = new PostgreSQL();
        try {
            String[] gasto = new PostgreSQL().buscarGasto(jTextField4.getText());
            jtextmonto.setText(gasto[1]);
            jtextDescrip.setText(gasto[2]);

            SimpleDateFormat formatoDelTexto = new SimpleDateFormat("yyyy-MM-dd");
            String strFecha = gasto[3];
            Date fecha = null;
            try {
                fecha = formatoDelTexto.parse(strFecha);
            } catch (ParseException ex) {
            }
            jFormattedTextField1.setText(fecha.toLocaleString().split(" ")[0]);

            int op = JOptionPane.showConfirmDialog(null, "Esta seguro de eliminar este gasto?", "Advertencia", JOptionPane.WARNING_MESSAGE);
            if (op == JOptionPane.YES_OPTION) {
                postgres.eliminarGasto(jTextField4.getText());
                JOptionPane.showMessageDialog(null, "Gasto Eliminado", "Ok", JOptionPane.INFORMATION_MESSAGE);
                this.limpiar();

            }
        } catch (NullPointerException e) {
            JOptionPane.showMessageDialog(null, "El id buscado no existe en su base de datos", "ERROR", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jButton2ActionPerformed

